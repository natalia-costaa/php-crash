<?php
// echo - Output strings, numbers, html, etc
    echo 123, "Hello", 10.8;

// print - Works like echo, but can only take in a single argument
    print 456;

// print_r() - print single values and arrays
    print_r([1,2,3]);

// var_dump() - Returns more info like data typer and lenght
    var_dump(true);
// var_export() - Similar to var_dump(). Outputs a string representation
//of a variable
    var_export('hello');
?>